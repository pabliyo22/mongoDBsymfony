<?php

namespace AppBundle\Document;

use Doctrine\ODM\MongoDB\Mapping\Annotations as MongoDB;

/** @MongoDB\Document */
class Counters {

    /**
     * @MongoDB\Id
     */
    private $id;

    /**
     *
     * @MongoDB\Field(type="timestamp")
     */
    private $dateSeqEnd;

    /**
     *
     * @MongoDB\Field(type="timestamp")
     */
    private $dateSeqStart;

    /** @MongoDB\Field(type="string") */
    private $jj;

    /** @MongoDB\Field(type="string") */
    private $ci;

    /** @MongoDB\Field(type="string") */
    private $co;

    /** @MongoDB\Field(type="string") */
    private $jp;


    /**
     * Get id
     *
     * @return id $id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set dateSeqEnd
     *
     * @param timestamp $dateSeqEnd
     * @return $this
     */
    public function setDateSeqEnd($dateSeqEnd)
    {
        $this->dateSeqEnd = $dateSeqEnd;
        return $this;
    }

    /**
     * Get dateSeqEnd
     *
     * @return timestamp $dateSeqEnd
     */
    public function getDateSeqEnd()
    {
        return $this->dateSeqEnd;
    }

    /**
     * Set dateSeqStart
     *
     * @param timestamp $dateSeqStart
     * @return $this
     */
    public function setDateSeqStart($dateSeqStart)
    {
        $this->dateSeqStart = $dateSeqStart;
        return $this;
    }

    /**
     * Get dateSeqStart
     *
     * @return timestamp $dateSeqStart
     */
    public function getDateSeqStart()
    {
        return $this->dateSeqStart;
    }

    /**
     * Set jj
     *
     * @param string $jj
     * @return $this
     */
    public function setJj($jj)
    {
        $this->jj = $jj;
        return $this;
    }

    /**
     * Get jj
     *
     * @return string $jj
     */
    public function getJj()
    {
        return $this->jj;
    }

    /**
     * Set ci
     *
     * @param string $ci
     * @return $this
     */
    public function setCi($ci)
    {
        $this->ci = $ci;
        return $this;
    }

    /**
     * Get ci
     *
     * @return string $ci
     */
    public function getCi()
    {
        return $this->ci;
    }

    /**
     * Set co
     *
     * @param string $co
     * @return $this
     */
    public function setCo($co)
    {
        $this->co = $co;
        return $this;
    }

    /**
     * Get co
     *
     * @return string $co
     */
    public function getCo()
    {
        return $this->co;
    }

    /**
     * Set jp
     *
     * @param string $jp
     * @return $this
     */
    public function setJp($jp)
    {
        $this->jp = $jp;
        return $this;
    }

    /**
     * Get jp
     *
     * @return string $jp
     */
    public function getJp()
    {
        return $this->jp;
    }
}
